# %: Es el residuo de la división, lo que te sobra.
4%2

# **: Exponente
4**2


# Estos  reasignan el valor de una variable:
a+=5 #le agrega 5 más lo que tenía antes la variable.
a-=5 #le quita 5 a lo que tenía antes la variable.
a*=5# multiplica por 5 lo que tenía la variable.
a/=5#Divide lo que tenía entre 5, mostrando los decimales.
a//=5#Divide lo que tenía entre 5, SIN los decimales.
a%=5#divide y reemplaza el contenido de la variable con el módulo de la variable.
a**=5#toma el valor de una variable y lo eleva a la 5ta potencia (reemplaza el valor de la variable)